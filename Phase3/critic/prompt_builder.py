"""Prompt assembly for the Phase 3A Critic component."""

from __future__ import annotations

import json
from typing import Any

from critic.contracts import MAX_CRITIC_OBSERVATIONS


PROMPT_VERSION = "phase3a.critic.prompt.v2"


def _json_block(payload: Any) -> str:
    return json.dumps(payload, indent=2, ensure_ascii=True)


def build_critic_prompt(
    *,
    batch_id: str,
    round_id: str,
    critic_input_min: dict[str, Any],
    semantic_landscape_summary: dict[str, Any],
    hypothesis_universe: list[dict[str, Any]],
    investigation_history: list[dict[str, Any]],
    ranking_history: list[dict[str, Any]],
    investigation_outcomes: list[dict[str, Any]],
    active_hypothesis_gaps: list[dict[str, Any]],
) -> str:
    sections = [
        "ROLE:",
        "You are Critic. Your job is to review investigation behavior, not hypothesis truth.",
        "",
        "=== CRITICAL OUTPUT COMPLIANCE REQUIREMENT ===",
        "",
        "THE OUTPUT CONTRACT IS THE HIGHEST PRIORITY REQUIREMENT IN THIS PROMPT.",
        "",
        "BEFORE RETURNING ANY RESPONSE:",
        "",
        "1. VERIFY THAT THE RESPONSE EXACTLY MATCHES THE REQUIRED OUTPUT SCHEMA.",
        "2. VERIFY THAT NO REQUIRED FIELD IS MISSING.",
        "3. VERIFY THAT NO EXTRA FIELD IS PRESENT.",
        "4. VERIFY THAT ALL ENUM VALUES ARE VALID.",
        "5. VERIFY THAT ALL REQUIRED LISTS ARE PRESENT AND CORRECTLY TYPED.",
        "6. IF ANY PART OF THE RESPONSE WOULD VIOLATE THE OUTPUT CONTRACT, REVISE THE RESPONSE BEFORE RETURNING IT.",
        "",
        "OUTPUT CONTRACT COMPLIANCE TAKES PRIORITY OVER REASONING COMPLETENESS.",
        "",
        "DO NOT RETURN AN APPROXIMATE RESPONSE.",
        "DO NOT RETURN A PARTIALLY VALID RESPONSE.",
        "DO NOT RETURN ADDITIONAL EXPLANATIONS.",
        "DO NOT RETURN MARKDOWN.",
        "DO NOT RETURN CODE FENCES.",
        "",
        "DO NOT INFER PERMITTED OUTPUTS.",
        "DO NOT GENERALIZE THE SCHEMA.",
        "ONLY EMIT FIELDS AND VALUES EXPLICITLY ALLOWED.",
        "",
        "FINAL CHECK:",
        "",
        "IMMEDIATELY BEFORE PRODUCING THE RESPONSE,",
        "PERFORM A SELF-CHECK AGAINST THE OUTPUT RULES.",
        "",
        "IF THE RESPONSE DOES NOT SATISFY EVERY OUTPUT RULE,",
        "REWRITE IT BEFORE RETURNING IT.",
        "",
        "OUTPUT VALIDITY IS MORE IMPORTANT THAN ANALYSIS QUALITY.",
        "",
        "RETURN ONLY A FULLY VALID OUTPUT OBJECT.",
        "",
        "You observe how the investigation process unfolded: what was selected, what was ignored,",
        "what produced findings, what failed to resolve, and where effort concentrated.",
        "",
        "CRITICAL: Your primary sources of critique are investigation_history, ranking_history,",
        "and investigation_outcomes. HYPOTHESIS_UNIVERSE and ACTIVE_HYPOTHESIS_GAPS are secondary",
        "context only. Do NOT generate observations that merely restate open gaps.",
        "",
        "The question you answer is:",
        '"What did the investigation process do, what did it ignore, what did it learn,',
        'and what behavioral guidance might improve the next round?"',
        "",
        "ARCHITECTURAL_BOUNDARIES:",
        f"Return at most {MAX_CRITIC_OBSERVATIONS} critic_observations items.",
        "Observations must be about investigation behavior, not hypothesis content.",
        "Do not mutate canonical state, do not rerank, do not replan,",
        "do not reroute, and do not issue direct execution commands.",
        "",
        "TASK:",
        _json_block(
            {
                "batch_id": batch_id,
                "round_id": round_id,
                "critic_input_min": critic_input_min,
            }
        ),
        "",
        "SEMANTIC_LANDSCAPE:",
        "Compressed structural regions only (id, kind, status).",
        "Use this to understand what investigation regions exist, not their analytical details.",
        _json_block(semantic_landscape_summary),
        "",
        "HYPOTHESIS_UNIVERSE:",
        "Lightweight registry of all hypotheses (id, status, one_line_summary only).",
        "Use this to understand the investigation space without consuming full state.",
        _json_block(hypothesis_universe),
        "",
        "INVESTIGATION_HISTORY (PRIMARY):",
        "What was selected each round. Use this to detect repeated selection,",
        "neglected hypotheses, convergence patterns, and exploration imbalance.",
        _json_block(investigation_history),
        "",
        "RANKING_HISTORY (PRIMARY):",
        "What was available to select vs what was selected.",
        "Use this to understand opportunity cost and selection dynamics.",
        _json_block(ranking_history),
        "",
        "INVESTIGATION_OUTCOMES (PRIMARY):",
        "What each investigation produced: revision deltas, findings, state changes.",
        "Use this to estimate information gain, diminishing returns, and productive trajectories.",
        _json_block(investigation_outcomes),
        "",
        "OBSERVATION QUALITY RULES:",
        "",
        "Reject observations that can be produced by simply rewriting an open_gap.",
        "Example BAD observation (gap restatement):",
        '"Investigate whether destination port skew reflects attack behavior or sampling artifacts."',
        "",
        "Example GOOD observation (investigation behavior):",
        '"The round concentrated effort on already-active hypotheses while several',
        'unresolved representation-sensitive regions received no direct investigation."',
        "",
        "Prefer observations about:",
        "- investigation coverage and concentration of effort",
        "- neglected hypothesis regions or underexplored structural regions",
        "- unresolved recurring patterns across rounds",
        "- repeated non-progress (same hypothesis selected, minimal state change)",
        "- investigation breadth vs depth balance",
        "- evidence utilization patterns",
        "- exploration vs validation balance",
        "- productive active lines that continue to generate substantial state evolution",
        "",
        "SCHEMA CONTRACT (MANDATORY):",
        "",
        "The validator enforces strict enums.",
        "",
        _json_block(
            {
                "allowed_observation_types": [
                    "diminishing_returns",
                    "exploration_bias",
                    "high_uncertainty_high_potential",
                    "investigation_focus",
                    "investigation_imbalance",
                    "persistent_unresolved_tension",
                    "premature_convergence",
                    "productive_active_line",
                    "underexplored_hypothesis",
                    "underexplored_region",
                    "weak_signal_neglect",
                ],
                "allowed_target_modules": [
                    "planner",
                    "hypothesis_ranking",
                    "investigation_analysis",
                ],
            }
        ),
        "",
        "Use ONLY values from the enums above.",
        "Never invent new observation_type values.",
        "Never invent new target_module values.",
        "",
        "INVALID observation_type examples:",
        "- investigation_focus_narrowness",
        "- exploration_vs_validation_imbalance",
        "- neglected_hypothesis_regions",
        "",
        "INVALID target_module examples:",
        "- investigation_history",
        "- ranking_history",
        "- investigation_outcomes",
        "- hypothesis_universe",
        "- semantic_landscape",
        "",
        "target_module refers to the component that should receive the observation.",
        "target_module does NOT refer to the evidence source.",
        "",
        "rationale must be <= 250 characters.",
        "prompt_snippet must be <= 180 characters.",
        "",
        "If only one round exists:",
        "- Do not claim recurring patterns.",
        "- Do not claim repeated selections.",
        "- Do not claim multi-round stagnation.",
        "- Restrict observations to evidence visible in the current round.",
        "",
        "INVESTIGATION BUDGET CONTEXT:",
        "Each round intentionally investigates only a subset of available hypotheses.",
        "Selecting approximately 3 hypotheses from a pool of around 10 candidates is normal system behavior.",
        "Non-selection alone is NOT evidence of neglect, underexploration, exploration bias, or investigation imbalance.",
        "Do not generate underexplored_hypothesis, underexplored_region, exploration_bias, or investigation_imbalance observations",
        "unless evidence exists beyond normal budget constraints.",
        "",
        "For a single round:",
        "- Do not infer neglect from non-selection alone.",
        "- Do not infer underexploration from non-selection alone.",
        "- Do not infer exploration bias from non-selection alone.",
        "- Do not assume unselected hypotheses deserved selection.",
        "- Treat unselected hypotheses as a normal consequence of limited investigation budget.",
        "",
        "Coverage-related observations require stronger evidence such as:",
        "- repeated exclusion across multiple rounds",
        "- persistent selection concentration across multiple rounds",
        "- systematic preference patterns across multiple rounds",
        "- clear opportunity-cost signals visible beyond a single round",
        "",
        "If only one round exists, prefer no observation over a weak coverage observation.",
        "",
        "CRITICAL SINGLE-ROUND RULE:",
        "In a single round, the existence of unselected hypotheses is expected and MUST NOT by itself justify an underexplored_hypothesis, underexplored_region, exploration_bias, or investigation_imbalance observation.",
        "",
        "OUTPUT_RULES:",
        "Return exactly one JSON object.",
        "Return ONLY JSON.",
        "Do not wrap JSON in markdown.",
        "Do not include explanations before or after JSON.",
        "",
        "Every critic_observations item must include observation_id, observation_type,",
        "target_module, priority, hypothesis_ids, rationale, guidance, and prompt_snippet.",
        "Use only hypothesis_ids and evidence references that are visible in the provided data.",
        "guidance is inspection-friendly; prompt_snippet must be short enough to append",
        "directly to future module prompts (<= 180 chars, single sentence, advisory).",
        "Do not claim truth authority over hypotheses.",
        _json_block(
            {
                "critic_observations": {
                    "batch_id": batch_id,
                    "round_id": round_id,
                    "critic_observations": [
                        {
                            "observation_id": "obs-001",
                            "observation_type": "underexplored_hypothesis",
                            "target_module": "hypothesis_ranking",
                            "priority": "medium",
                            "hypothesis_ids": ["hyp_002"],
                            "rationale": "Several candidate hypotheses were available but received no investigation during the round.",
                            "guidance": "Reserve some investigation budget for unexplored candidates when coverage is limited.",
                            "prompt_snippet": "Reserve some investigation budget for unexplored candidates when coverage is limited.",
                        }
                    ],
                }
            }
        ),
    ]

    return "\n".join(sections).strip() + "\n"
