        "",
        "Aggregation should evaluate worker evidence against the",
        "original hypothesis intent.",
        "",
        "Strategic objective defines what the hypothesis was",
        "attempting to determine.",
        "",
        "Key checks define the major questions that were investigated.",
        "",
        "Success criteria define what evidence would support or",
        "weaken the hypothesis.",
        "",
        _json_block(
            {
                "strategic_objective":
                    normalized_inputs.get("strategic_objective", ""),
                "key_checks":
                    normalized_inputs.get("key_checks", []),
                "success_criteria":
                    normalized_inputs.get("success_criteria", []),
            }
        ),
        "",
        "=== TASK INTENT CONTEXT ===",
        "",
        "Aggregation should evaluate evidence relative to the",
        "investigation objective of each task.",
        "",
        "For each task consider:",
        "",
        "* what the task attempted to determine",
        "* what evidence was collected",
        "* whether the task objective appears resolved",
        "* whether the evidence supports, weakens, or leaves",
        "  unresolved the investigated question",
        "",
        "If task metadata exists inside normalized_inputs,",
        "render it.",
        "",
        "TASK_CONTEXT:",
        _json_block(normalized_inputs.get("task_context", [])),
        "",
        "SOURCE_WORKER_RESULTS:",
        _json_block(normalized_inputs.get("worker_results", [])),
        "",
        "=== TASK-AWARE SYNTHESIS ===",
        "",
        "Each worker task was created to investigate a different aspect",
        "of the same hypothesis.",
        "",
        "For each task:",

        "- identify what it attempted to determine",
        "- identify what evidence it produced",
        "- identify whether its investigation objective appears resolved",

        "Then evaluate the hypothesis as a whole.",

        "Do not treat worker findings as independent lists.",
        "Treat them as evidence produced by coordinated investigations.",
