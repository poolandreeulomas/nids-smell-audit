        "",
        "Avoid low-impact implementation limitations.",
        "",
        "=== SHORTCUT EVIDENCE ESCALATION ===",

        "When multiple worker tasks report:",

        "- low_entropy",
        "- dominant_value",
        "- high_class_separation",
        "- shortcut-like behavior",
        "- predictive leverage",
