        "Repeated evidence should increase confidence,",
        "not merely increase finding count.",
        "",
        "Aggregation may generate additional merged findings",
        "that describe evidence convergence.",
        "",
        "=== TRACEABILITY PRESERVATION ===",
        "",
        "Worker findings are primary evidence.",
        "",
        "Do not remove worker findings merely because",
        "multiple tasks reported similar observations.",
        "",
        "Preserve traceability.",
        "",
        "Convergence findings should be added in addition",
        "to worker findings, not replace them.",
        "",
        "Preserve evidence lineage whenever possible.",
        "",
        "Evidence preservation has priority over compression.",
        "",
